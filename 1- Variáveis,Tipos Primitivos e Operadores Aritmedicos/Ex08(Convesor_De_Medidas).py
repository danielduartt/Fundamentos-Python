
#crie um programa que receba um número e metros e faça a conversão da medidas
metros = float(input("Digite uma distancia em metros: "))
print(f"Em km: {metros / 1000}")
print(f"Em hm: {metros / 100}")
print(f"Em dam: {metros / 10}")
print(f"Em m: {metros * 100}")
print(f"Em dm: {metros * 10}")
print(f"Em cm: {metros * 100}")
print(f"Em mm: {metros * 1000}")
